#  и минимальным значением дробной части элементов.
# *Пример:*
# - [1.1, 1.2, 3.1, 5, 10.01] => 0.19 (максимальное значение у числа 1.2 , минимальное у 10.01)
# отделить дробную часть, чтобы найти мин и мах дробной части

print()
print('Задача 3')

list1 = [1.1, 1.2, 3.1, 5, 10.01]
print(list1)
list2 = [round(i % 1, 2) for i in list1 if i % 1 != 0]
print(list2)
print(max(list2) - min(list2))


