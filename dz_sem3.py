# 2'. Напишите программу, которая найдёт произведение пар чисел списка. Парой считаем первый и последний элемент, второй и предпоследний и т.д.
# *Пример:*
# - [2, 3, 4, 5, 6] => [12, 15, 16];
# - [2, 3, 5, 6] => [12, 15]

print()
print('Задача 2')

list1 = [2, 3, 4, 5, 6]
print(list1)
list2 = []

while len(list1) > 0:
    if len(list1) == 1:
        list2.append(list1.pop()**2)
    else:
        list2.append(list1.pop(0)*list1.pop())  
print(list2)    



